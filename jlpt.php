                <h2>JLPT LEVELS</h2>
                <p>The JLPT has five levels: N1, N2, N3, N4 and N5. The easiest level is N5 and the most difficult level
                    is N1.</p>
                <img src="images/jlpt.gif" alt="">
                <p>N4 and N5 measure the level of understanding of basic Japanese mainly learned in class. N1and N2
                    measure the level of understanding of Japanese used in a broad range of scenes in actual everyday
                    life. N3 is a bridging level between N1/N2 and N4/N5.<br></p>

                <h2>SUMMARY OF JLPT LEVELS:</h2>
                <p><b>JLPT N5:</b> One is able to listen and comprehend conversations about topics regularly encountered in
                    daily life and classroom situations, and is able to pick up necessary information from short
                    conversations spoken slowly. One is able to read and understand typical expressions and sentences
                    written in hiragana, katakana, and basic kanji.<br><br>

                    <b>JLPT N4:</b> One is able to listen and comprehend conversations encountered in daily life and generally
                    follow their contents, provided that they are spoken slowly One is able to read and understand
                    passages on familiar daily topics written in basic vocabulary and kanji. One is able to listen and
                    comprehend conversations encountered in daily life and generally follow their contents, provided
                    that they are spoken slowly.<br><br>

                    <b>JLPT N3:</b> One is able to read and understand written materials with specific contents concerning
                    everyday topics. One is also able to grasp summary information such as newspaper headlines. In
                    addition, one is also able to read slightly difficult writings encountered in everyday situations
                    and understand the main points of the content if some alternative phrases are available to aid one’s
                    understanding. One is able to listen and comprehend coherent conversations in everyday situations,
                    spoken at near-natural speed, and is generally able to follow their contents as well as grasp the
                    relationships among the people involved.<br><br>

                    <b>JLPT N2:</b> One is able to read materials written clearly on a variety of topics, such as articles and
                    commentaries in newspapers and magazines as well as simple critiques, and comprehend their contents.
                    One is also able to read written materials on general topics and follow their narratives as well as
                    understand the intent of the writers. One is able to comprehend orally presented materials such as
                    coherent conversations and news reports, spoken at nearly natural speed in everyday situations as
                    well as in a variety of settings, and is able to follow their ideas and comprehend their contents.
                    One is also able to understand the relationships among the people involved and the essential points
                    of the presented materials.<br><br>

                    <b>JLPT N1:</b> One is able to read writings with logical complexity and/or abstract writings on a variety
                    of topics, such as newspaper editorials and critiques, and comprehend both their structures and
                    contents.<br><br>

                    One is also able to read written materials with profound contents on various topics and follow their
                    narratives as well as understand the intent of the writers comprehensively. ・One is able to
                    comprehend orally presented materials such as coherent conversations, news reports, and lectures,
                    spoken at natural speed in a broad variety of settings, and is able to follow their ideas and
                    comprehend their contents comprehensively. One is also able to understand the details of the
                    presented materials such as the relationships among the people involved, the logical structures, and
                    the essential points.<br><br>

