                <p>The Japanese-Language Proficiency Test (JLPT) has been offered by the Japan Foundation and Japan
                    Educational Exchanges and Services (formerly Association of International Education, Japan) since
                    1984 as a reliable means of evaluating and certifying the Japanese proficiency of non-native
                    speakers. At the beginning, there were approximately 7,000 examinees worldwide. In 2011, there were
                    as many as 610,000 examinees around the globe, making JLPT the largest-scale Japanese-language test
                    in the world. JLPT results has expanded from skill measurement to include employment screening and
                    evaluation for promotions and pay raises as well as use as a form of qualification. The Japan
                    Foundation and Japan Educational Exchanges and Services have been jointly conducting the test.<br>
